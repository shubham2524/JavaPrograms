class method_overloading 
{


	public static void method1()
	{
		System.out.println("this is static-method1 without parameter");
	}
	public static void method1(int a)
	{
		
		System.out.println("this is static-method1 with 1- parameter");
	}

	public  void method12()
	{
		
		System.out.println("this is non-static method1 ");
	}

	public  void method12(int a)
	{
		
		System.out.println("this is non-ststic-method1 with parameter");
	}
	public static void main(String[] args) 
	{
		method1();
		method1(2);


		method_overloading obj = new method_overloading();//object creation

		obj.method12();
		obj.method12(2);
	}
}
